public class MathClassMethods {
    public static void main(String[] args) {
        System.out.println("The absolute value of -124.44 is " + Math.abs(-124.44));

        System.out.println("The ceiling value of -124.44 is " + Math.ceil(-124.44));

        System.out.println("The floor value of -124.44 is " + Math.floor(-124.44));

        System.out.println("Returns the maximum of -122.44 and 5.4 " + Math.max(-124.44, 5.4));

        System.out.println("Returns the minimum of -122.44 and 5.4 " + Math.min(-124.44, 5.4));

        System.out.println("Returns 5 to the power of 3 " + Math.pow(5, 3));

        System.out.println("Returns the square root of 9 is " + Math.sqrt(9));
    }
}
